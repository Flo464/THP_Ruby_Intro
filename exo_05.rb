# le signe #{} est utilisé pour concatener deux éléments ici du texte et une opération.
puts "On va compter le nombre d'heures de travail à THP" #affiche la phrase entre guillemets
puts "Travail : #{10 * 5 * 11}"#affiche de la phrase plus le résultat de l'opération concatener
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"#affiche de la phrase plus le résultat de l'opération concatener

puts "Et en secondes ?" #affiche la phrase entre guillemets

puts 10 * 5 * 11 * 60 * 60#affiche le résultat de l'opération

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" #affiche la phrase entre guillemets

